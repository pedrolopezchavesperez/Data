    private void SiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SiguienteActionPerformed
        aplicacion.Actividad act = fa.obtenerActividad((String)jComboBox1.getSelectedItem()).get(jTable1.getSelectedRow());
        aplicacion.Organizar actividad;
        String horario = jTextField1.getText();
        String dni = fa.getSesionMonitor().getDni();
        String especialidad = fa.getSesionMonitor().getEspecialidad();
        ArrayList <String> aux = new ArrayList <> ();
        aux.add (dni);
        if(especialidad.equals(act.getCampo())){
            ErrorE.setVisible(false);
            actividad = new aplicacion.Organizar(act.getNombre(), horario, act.getNombreInstalacion(), aux, act.getPlazas());
            if(fa.organizarActividad(actividad)){
                this.dispose();
            }
            else{
                ErrorH.setVisible(true);
            }
        }
        else{
            ErrorE.setVisible(true);
        }
        
    }//GEN-LAST:event_SiguienteActionPerformed

