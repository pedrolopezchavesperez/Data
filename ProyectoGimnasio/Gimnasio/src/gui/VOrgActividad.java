    private void buscarActividades (){
        ModeloTablaActividad m = (ModeloTablaActividad) jTable1.getModel();
        m.setFilas (fa.obtenerActividad ((String)jComboBox1.getSelectedItem()));
        if (m.getRowCount() > 0) 
            jTable1.setRowSelectionInterval(0, 0);
    }
    
    private void rellenarComboBox (){
        jComboBox1.addItem ("Todas");
        for (aplicacion.Instalacion e : fa.obtenerInstalaciones())
            jComboBox1.addItem(e.getNombre());
    }



